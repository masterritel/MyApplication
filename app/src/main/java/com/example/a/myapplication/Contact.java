package com.example.a.myapplication;

public class Contact {
     int id;
    String nom ;
     String num;


    public void setId(int id)
    {
        this.id = id;
    }

    public int getId()
    {
        return id;
    }
    public void setNom(String nom)
    {
        this.nom = nom;
    }

    public String getNom()
    {
        return nom;
    }


    public void setNum(String num)
    {
        this.num = num;
    }

    public String getNum()
    {
        return num;
    }

}






